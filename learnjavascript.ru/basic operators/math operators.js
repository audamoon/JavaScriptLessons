console.log("Modulus and Exponentiation operators:");
console.log(20%6) //Modulus operator
console.log(2**4,"\n") //Exponentiation operator

console.log("Increment and Decrement operators:");
let increment = 5;
increment++;
console.log(increment) //Increment operator
let decrement = 5;
decrement--;
console.log(decrement,"\n"); //Decrement operator

console.log("Adding String and Numbers:");
console.log(typeof(5+5)); //returns Number 55
console.log(typeof("5" + 5)); //returns String "55"
console.log(typeof("Hi"+ 5),"\n"); //returns String "Hi5"

console.log("Unary plus:");
let unaryPlus = "45";
unaryPlus = +unaryPlus;
console.log(typeof(unaryPlus));//returns number

